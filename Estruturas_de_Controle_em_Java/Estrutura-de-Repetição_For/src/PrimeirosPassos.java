class PrimeirosPassos {
    public static void main(String[] args) {

        /**
         * De 1 á 10 de forma decrescente:
         * ainda estamos em Primeiros Passos:
         * 
         *  utilize for quando você sabe a quantidade de repetições.
         * 
         */
        
        for(int x = 10; x >= 1; x--) {
            System.out.println(x);
        }

        /**
         * Imprimindo uma frase varias vesses:
         * 
         */

        for(int y = 1; y <= 5 ; y++) {
            System.out.println("Eu Estou Aprendendo Java! ");

        }


    }





}